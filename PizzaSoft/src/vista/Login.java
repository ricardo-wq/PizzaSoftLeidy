    private boolean hayUsuarios(Connection conn) throws SQLException {
        String consultaUsuarios = "SELECT * FROM usuarios";
        Statement stmt = conn.createStatement();
        ResultSet rs = stmt.executeQuery(consultaUsuarios);

        boolean existenUsuarios = rs.next(); // true si hay al menos un registro
        rs.close();
        stmt.close();

        return existenUsuarios;
    }

    private void crearPrimerUsuario(Connection conn) throws SQLException {
        boolean existenDatosValidos = false;

        JOptionPane.showMessageDialog(
                null,
                "¡Bienvenido al Sistema de Pedidos de Pizza!\nGracias por usar nuestro software.\nVamos a crear el primer usuario para ingresar al sistema...",
                "Bienvenida",
                JOptionPane.INFORMATION_MESSAGE
        );

        while (!existenDatosValidos) {
            String nuevoUsuario = JOptionPane.showInputDialog(
                    null,
                    "Ingrese un nombre de usuario:",
                    "Crear primer usuario",
                    JOptionPane.WARNING_MESSAGE
            );

            String nuevaContraseña = JOptionPane.showInputDialog(
                    null,
                    "Ingrese una contraseña:",
                    "Crear primer usuario",
                    JOptionPane.WARNING_MESSAGE
            );

            // Validar que no sean null, vacíos ni "-1"
            if (nuevoUsuario != null && !nuevoUsuario.trim().isEmpty()
                    && nuevaContraseña != null && !nuevaContraseña.trim().isEmpty()
                    && !nuevaContraseña.trim().equals("-1")) {

                String insertarUsuario = "INSERT INTO usuarios (usuario, contraseña, rol) VALUES (?, ?, ?)";
                PreparedStatement psInsert = conn.prepareStatement(insertarUsuario);
                psInsert.setString(1, nuevoUsuario.trim());
                psInsert.setString(2, hashSHA256(nuevaContraseña));
                psInsert.setString(3, "Administrador");
                psInsert.executeUpdate();
                psInsert.close();

                JOptionPane.showMessageDialog(
                        null,
                        "Primer usuario creado correctamente.\nIngrese a continuación.",
                        "Éxito",
                        JOptionPane.INFORMATION_MESSAGE
                );
                existenDatosValidos = true;  // Sale del loop

            } else {
                JOptionPane.showMessageDialog(
                        null,
                        "Debe ingresar un usuario y una contraseña válidos.",
                        "Advertencia",
                        JOptionPane.WARNING_MESSAGE
                );
                // El loop continúa
            }
        }
    }

    public static String hashSHA256(String input) {
        try {
            MessageDigest digest = MessageDigest.getInstance("SHA-256");
            byte[] hash = digest.digest(input.getBytes(StandardCharsets.UTF_8));
            StringBuilder hexString = new StringBuilder();
            for (byte b : hash) {
                hexString.append(String.format("%02x", b));
            }
            return hexString.toString();
        } catch (NoSuchAlgorithmException e) {
            throw new RuntimeException("Error al encriptar con SHA-256", e);
        }
    }


    private void jButton_iniciarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_iniciarActionPerformed

        if (txt_password.getText().isEmpty() || txt_usuario.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Rellene todos los campos");
        } else {

            String user = txt_usuario.getText().trim();
            String password = hashSHA256(txt_password.getText().trim());
            GestionPizzasConexion conexion = new GestionPizzasConexion();

            try {

                Connection conn = conexion.Conectar();
                // Proceder a verificar las credenciales
                String consulta = "SELECT id_usuario, usuario, contraseña FROM usuarios WHERE usuario = ? AND contraseña = ?";
                PreparedStatement prst = conn.prepareStatement(consulta);

                prst.setString(1, user);
                prst.setString(2, password);

                ResultSet rs = prst.executeQuery();

                if (rs.next()) {
                    idUsuario = rs.getInt("id_usuario");
                    

                    this.dispose();
                    Sistema sis = new Sistema(idUsuario);
                    sis.setVisible(true);
                } else {
                    JOptionPane.showMessageDialog(null, "Credenciales incorrectas", "Error", JOptionPane.ERROR_MESSAGE);
                }

                rs.close();
                prst.close();
                conexion.CerrarConexion();

            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, e, "Error", JOptionPane.ERROR_MESSAGE);
            }
        }

    }//GEN-LAST:event_jButton_iniciarActionPerformed


