    /**
     * Método para actualizar la barra de progreso.
     */
    public void Actualizarbarra(int porcentaje, String mensaje) {

        label_mensaje.setVisible(true);
        jLabel_porcentaje.setVisible(true);
        if (porcentaje < 0 || porcentaje > 100) {
            JOptionPane.showMessageDialog(
                    this,
                    "Porcentaje inválido: " + porcentaje,
                    "Error de progreso",
                    JOptionPane.ERROR_MESSAGE
            );
            return;
        }
        if (mensaje == null || mensaje.trim().isEmpty()) {
            mensaje = "Procesando...";
        }
        if (!barra.isVisible()) {
            barra.setVisible(true);
        }
        barra.setValue(porcentaje);
        label_mensaje.setText(mensaje);
        jLabel_porcentaje.setText(porcentaje + "%");
        barra.repaint();

        if (porcentaje == 100) {
            // Oculta la barra tras medio segundo para que se vea el 100%
            new Timer(2000, e -> barra.setVisible(false)).start();
            new Timer(2000, e -> label_mensaje.setVisible(false)).start();
            new Timer(2000, e -> jLabel_porcentaje.setVisible(false)).start();

        }

    }

    /**
     * Lógica normal de login/primer usuario (antes estaba en el constructor).
     */
    public void verificarYCrearPrimerUsuario() {
        GestionPizzasConexion conexion = new GestionPizzasConexion();
        Connection conn = conexion.Conectar();
        boolean existenUsuarios = false;

        try {
            existenUsuarios = hayUsuarios(conn);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "❌ Error al comprobar usuarios: " + e.getMessage(),
                    "Error", JOptionPane.ERROR_MESSAGE);
            conexion.CerrarConexion();
            return;
        }

        if (!existenUsuarios) {
            try {
                crearPrimerUsuario(conn);
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(this, "❌ Error al crear primer usuario: " + e.getMessage(),
                        "Error", JOptionPane.ERROR_MESSAGE);
                conexion.CerrarConexion();
                return;
            }
        }
        conexion.CerrarConexion();
    }

